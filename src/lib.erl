-module(lib).

-compile(export_all).

%% -----------------------------------------------------------------------------
%% Generating TS for Hibari with microseconds
%% -----------------------------------------------------------------------------

make_ts() ->
	{M, S, MS} = now(),
	(M * 1000000 + S) * 1000000 + MS.
